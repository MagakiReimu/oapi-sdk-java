package com.lark.oapi.sample.apiall.corehrv1;

import com.lark.oapi.Client;
import com.lark.oapi.core.utils.Jsons;
import com.lark.oapi.service.corehr.v1.model.DeleteContractReq;
import com.lark.oapi.service.corehr.v1.model.DeleteContractResp;

// DELETE /open-apis/corehr/v1/contracts/:contract_id
public class DeleteContractSample {

    public static void main(String arg[]) throws Exception {
        // 构建client
        Client client = Client.newBuilder("appId", "appSecret").build();

        // 创建请求对象
        DeleteContractReq req = DeleteContractReq.newBuilder()
                .contractId("4137834332")
                .build();

        // 发起请求
        DeleteContractResp resp = client.corehr().contract().delete(req);

        // 处理服务端错误
        if (!resp.success()) {
            System.out.println(String.format("code:%s,msg:%s,reqId:%s"
                    , resp.getCode(), resp.getMsg(), resp.getRequestId()));
            return;
        }

        // 业务数据处理
        System.out.println(Jsons.DEFAULT.toJson(resp.getData()));
    }
}
