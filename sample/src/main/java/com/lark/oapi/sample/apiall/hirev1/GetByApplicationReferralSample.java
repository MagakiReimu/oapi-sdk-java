package com.lark.oapi.sample.apiall.hirev1;

import com.lark.oapi.Client;
import com.lark.oapi.core.utils.Jsons;
import com.lark.oapi.service.hire.v1.model.GetByApplicationReferralReq;
import com.lark.oapi.service.hire.v1.model.GetByApplicationReferralResp;

// GET /open-apis/hire/v1/referrals/get_by_application
public class GetByApplicationReferralSample {

    public static void main(String arg[]) throws Exception {
        // 构建client
        Client client = Client.newBuilder("appId", "appSecret").build();

        // 创建请求对象
        GetByApplicationReferralReq req = GetByApplicationReferralReq.newBuilder()
                .applicationId("6134134355464633")
                .userIdType("open_id")
                .build();

        // 发起请求
        GetByApplicationReferralResp resp = client.hire().referral().getByApplication(req);

        // 处理服务端错误
        if (!resp.success()) {
            System.out.println(String.format("code:%s,msg:%s,reqId:%s"
                    , resp.getCode(), resp.getMsg(), resp.getRequestId()));
            return;
        }

        // 业务数据处理
        System.out.println(Jsons.DEFAULT.toJson(resp.getData()));
    }
}
